function classNames(...classes: string[]) {
  return classes.filter(Boolean).join(' ')
}

export function AdminSidebar() {
  const pathname = usePathname()

  return (
    <div className="flex grow flex-col gap-y-5 overflow-y-auto bg-zinc-900 px-6 pb-4">
      <div className="flex h-16 shrink-0 items-center">
        <Link href="/admin" className="flex items-center gap-2">
          <span className="text-xl font-bold text-white">Chisholm FC</span>
          <span className="rounded bg-emerald-600 px-2 py-0.5 text-xs font-medium text-white">
            Admin
          </span>
        </Link>
      </div>
      <nav className="flex flex-1 flex-col">
        <ul role="list" className="flex flex-1 flex-col gap-y-7">
          <li>
            <ul role="list" className="-mx-2 space-y-1">
              {navigation.map((item) => {
                const isActive = pathname === item.href ||
                  (item.href !== '/admin' && pathname.startsWith(item.href))
                return (
                  <li key={item.name}>
                    <Link
                      href={item.href}
                      className={classNames(
                        isActive
                          ? 'bg-zinc-800 text-white'
                          : 'text-zinc-400 hover:bg-zinc-800 hover:text-white',
                        'group flex gap-x-3 rounded-md p-2 text-sm font-semibold'
                      )}
                    >
                      <item.icon
                        className={classNames(
                          isActive ? 'text-white' : 'text-zinc-400 group-hover:text-white',
                          'h-6 w-6 shrink-0'
                        )}
                      />
                      {item.name}
                    </Link>
                  </li>
                )
              })}
            </ul>
          </li>
          <li>
            <div className="text-xs font-semibold text-zinc-400">Settings</div>
            <ul role="list" className="-mx-2 mt-2 space-y-1">
              {secondaryNavigation.map((item) => {
                const isActive = pathname === item.href
                return (
                  <li key={item.name}>
                    <Link
                      href={item.href}
                      className={classNames(
                        isActive
                          ? 'bg-zinc-800 text-white'
                          : 'text-zinc-400 hover:bg-zinc-800 hover:text-white',
                        'group flex gap-x-3 rounded-md p-2 text-sm font-semibold'
                      )}
                    >
                      <item.icon
                        className={classNames(
                          isActive ? 'text-white' : 'text-zinc-400 group-hover:text-white',
                          'h-6 w-6 shrink-0'
                        )}
                      />
                      {item.name}
                    </Link>
                  </li>
                )
              })}
            </ul>
          </li>
          <li className="mt-auto">
            <button
              onClick={() => signOut({ callbackUrl: '/' })}
              className="group -mx-2 flex w-full gap-x-3 rounded-md p-2 text-sm font-semibold text-zinc-400 hover:bg-zinc-800 hover:text-white"
            >
              <LogoutIcon className="h-6 w-6 shrink-0 text-zinc-400 group-hover:text-white" />
              Sign out
            </button>
          </li>
        </ul>
      </nav>
    </div>
  )
}

